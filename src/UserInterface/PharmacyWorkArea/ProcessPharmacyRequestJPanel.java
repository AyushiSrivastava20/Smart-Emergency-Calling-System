    public void populate(){
      DefaultTableModel model = (DefaultTableModel) medJTable.getModel();
      model.setRowCount(0);
      for(Medication med : request.getPatient().getMedicationDirectory().getMedicationList()){
          
          Object[] row = new Object[3];
          row[0] = med;
          row[1]=med.getMedication_description();
          row[2]=med.getMedication_type();
          model.addRow(row);
      }
        
        
    }
    
