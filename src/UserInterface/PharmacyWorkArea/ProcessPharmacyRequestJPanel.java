    private void searchJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchJButtonActionPerformed
        // TODO add your handling code here:
        
        if(idJTextField.getText().trim().length()==0){
           JOptionPane.showMessageDialog(null, "Please enter value to search", "Error", JOptionPane.ERROR_MESSAGE); 
           return;
        }
        String id = idJTextField.getText();
        if(enterprise instanceof HospitalEnterprise){
            HospitalEnterprise he = (HospitalEnterprise) enterprise;
            Drug drug = he.getDrugCatalog().search(id);
            if(drug==null){
              JOptionPane.showMessageDialog(null, "No drug found. Enter proper search ID", "Error", JOptionPane.ERROR_MESSAGE);   
            }
            else{
                drugNameJTextField.setText(drug.getDrugName());
                availabilityJTextField.setText(String.valueOf(drug.getCost()));
                costJTextField.setText(String.valueOf(drug.getCost()));
            }
            
        }
        
        
       
        
    }//GEN-LAST:event_searchJButtonActionPerformed

    private void orderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderActionPerformed
        // TODO add your handling code here:
        String id1 = drugNameJTextField.getText().trim();
        int selectedRow = medJTable.getSelectedRow();
         if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select row","Warning", JOptionPane.WARNING_MESSAGE);
            }
        else{
        Medication med = (Medication) medJTable.getValueAt(selectedRow, 0);
        if(quantityJTextField.getText().length()==0){
           JOptionPane.showMessageDialog(null, "Please enter Quantity", "Error", JOptionPane.ERROR_MESSAGE); 
           return; 
        }
        int quantity = Integer.parseInt(quantityJTextField.getText());
        int avail = Integer.parseInt(availabilityJTextField.getText());
        int cost= Integer.parseInt(costJTextField.getText());
         if(quantity>=avail){
             JOptionPane.showMessageDialog(null, "Quantity is greater than Availability! ", "Error", JOptionPane.ERROR_MESSAGE); 
             return;
            }
      
            int totPrice= quantity*cost;
            med.setCost(totPrice);
            
            if(enterprise instanceof HospitalEnterprise){
            HospitalEnterprise he = (HospitalEnterprise) enterprise;
           
             try{
                  Drug drug1 = he.getDrugCatalog().search(id1);
               drug1.setAvailability(avail-quantity);
            }
            catch (Exception e){}
            
            
            
            }
            
           
            
        
         }
         
        
    }//GEN-LAST:event_orderActionPerformed


