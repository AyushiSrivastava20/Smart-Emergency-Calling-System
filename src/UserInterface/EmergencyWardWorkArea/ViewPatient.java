    public void populateVitalSign(){
       DefaultTableModel dtm = (DefaultTableModel)viewVitalJTable.getModel();
       dtm.setRowCount(0);
        for (VitalSign vs: patient.getVitalSignHistory().getVitalSignList()){
            
            Object row[]= new Object[2];
            row[0]=vs;
            row[1]=vs.getStatus();
            dtm.addRow(row);
        }
        
    }
    

