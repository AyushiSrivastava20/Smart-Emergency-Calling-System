    private void viewVitalSignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewVitalSignJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = viewVitalJTable.getSelectedRow();

        if(selectedRow>=0){
            VitalSign vitalSign = (VitalSign)viewVitalJTable.getValueAt(selectedRow,0);
            ViewVitalSign vpvs = new ViewVitalSign(userProcessContainer, vitalSign, patient);
            userProcessContainer.add("ViewVitalSign", vpvs);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        }

        else{
            JOptionPane.showMessageDialog(null,"Please Select a row from the table first","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_viewVitalSignJButtonActionPerformed


