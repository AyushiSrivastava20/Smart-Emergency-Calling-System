    private void saveUPJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveUPJButtonActionPerformed
        // TODO add your handling code here:

        if(nameJTextField.getText().trim().length()==0 ||roomJTextField.getText().trim().length()==0 
                ||  statusJTextField.getText().trim().length()==0 || ageJTextField.getText().trim().length()==0 ){
             JOptionPane.showMessageDialog(null,"Please Enter all the fields", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
        try{
            String name = nameJTextField.getText().trim();
            String room  = roomJTextField.getText().trim();
            String status= statusJTextField.getText().trim();
            int age = Integer.parseInt(ageJTextField.getText().trim());
            patient.setPatientName(name);
            patient.setRoom_no(room);
            patient.setStatus(status);
            patient.setAge(age);
            JOptionPane.showMessageDialog(null,"Patient updated Successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
            saveUPJButton.setEnabled(false);
            updateJButton.setEnabled(true);
            nameJTextField.setEnabled(false);
            roomJTextField.setEnabled(false);
            statusJTextField.setEnabled(false);
            ageJTextField.setEnabled(false);

        }catch(NumberFormatException e){

            JOptionPane.showMessageDialog(null,"Correct Format Reuired", "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveUPJButtonActionPerformed


