    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        // TODO add your handling code here:
        
        if(nameJTextField.getText().length()==0 || noJTextField.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter all the fields","Error", JOptionPane.ERROR_MESSAGE); 
            return;
        }
        
        FireBrigade fb = new FireBrigade();
        fb.setBrigadeName(nameJTextField.getText().trim());
        fb.setBrigadeNo(noJTextField.getText().trim());
        fb.setStatus("Available");
        organization.getFireBrigadeQ().add(fb);
        JOptionPane.showMessageDialog(null,"Fire Brigade has been successfully created");
        nameJTextField.setText("");
        noJTextField.setText("");
        populateBrigade();
        
    }//GEN-LAST:event_addJButtonActionPerformed

