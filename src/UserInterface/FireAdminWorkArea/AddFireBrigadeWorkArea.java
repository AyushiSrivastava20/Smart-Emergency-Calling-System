    public void populateBrigade(){
        DefaultTableModel model = (DefaultTableModel) brigadeJTable.getModel();

        model.setRowCount(0);
        
        for (FireBrigade fb : organization.getFireBrigadeQ()){
            Object[] row = new Object[3];
            row[0]=fb;
            row[1]=fb.getBrigadeNo();
            row[2]=fb.getStatus();
            model.addRow(row);
        
            }
    }
