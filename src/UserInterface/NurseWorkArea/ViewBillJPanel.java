    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) billJTable.getModel();
        dtm.setRowCount(0);
        int total = 0;
        for (Medication med : patient.getMedicationDirectory().getMedicationList()) {
            Object[] row = new Object[3];
            row[0] = med.getMedication_type();
            row[1] = med.getMedication_description();
            row[2] = med.getCost();
            total += med.getCost();
            dtm.addRow(row);
        }
        int amt = 500 + total;
        patient.setTotal(amt);
        amountJTextField.setText(String.valueOf(amt));
    }

