    private void generatePDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generatePDFActionPerformed
        try {
            // TODO add your handling code here:
            Document doc = new Document();
            try {
                PdfWriter pdfWriter = PdfWriter.getInstance(doc, new FileOutputStream(patient.getPatientName() + ".pdf"));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(ViewBillJPanel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (DocumentException ex) {
                Logger.getLogger(ViewBillJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            doc.open();
            Paragraph paragraph1 = new Paragraph();
            paragraph1.add("HOSPITAL BILL");
            paragraph1.setAlignment(Element.ALIGN_CENTER);
            doc.add(paragraph1);
            doc.add(Chunk.NEWLINE);
            doc.add(new LineSeparator());
            doc.add(Chunk.NEWLINE);
            doc.add(Chunk.NEWLINE);
            Paragraph paragraph2 = new Paragraph();
            paragraph2.add("Patient Name : " + patient.getPatientName());
            paragraph2.setAlignment(Element.ALIGN_LEFT);
            doc.add(paragraph2);
            doc.add(Chunk.NEWLINE);
            Paragraph paragraph3 = new Paragraph();
            paragraph3.add("Patient Room : " + patient.getRoom_no());
            paragraph3.setAlignment(Element.ALIGN_LEFT);
            doc.add(paragraph3);
            doc.add(Chunk.NEWLINE);
            doc.add(Chunk.NEWLINE);
            PdfPTable table = new PdfPTable(3);

            PdfPCell cell;

            cell = new PdfPCell(new Phrase("Patient Bill Details"));
            cell.setColspan(3);
            table.addCell(cell);
          
            table.addCell("Medication Type");
            table.addCell("Description");
            table.addCell("Price");
            
            for(Medication med : patient.getMedicationDirectory().getMedicationList()){
                table.addCell(med.getMedication_description());
                table.addCell(med.getMedication_description());
                table.addCell(String.valueOf(med.getCost()));
            }
            
            cell = new PdfPCell(new Phrase("Hospital fees"));
            cell.setColspan(2);
            table.addCell(cell);
            table.addCell("500");
            cell = new PdfPCell(new Phrase("Total Amount"));
            cell.setColspan(2);
            table.addCell(cell);
            table.addCell(String.valueOf(patient.getTotal()));
             doc.add(table);
            doc.add(Chunk.NEWLINE);
            doc.add(Chunk.NEWLINE);
            
            Paragraph paragraph4 = new Paragraph();
            paragraph4.add("Thankyou for choosing our hospitalfor your healthcare needs. We have billed your Insurance and the balance due is your responsibility."
                    + "Please contact our customer care department at (857) 800-3978 with any questions or to set your payments. Thank you ");
            paragraph4.setAlignment(Element.ALIGN_JUSTIFIED);
            doc.add(paragraph4);
            
            
            
            doc.close();
            JOptionPane.showMessageDialog(null, "PDF Generated plese check the path to find the document", "Information", JOptionPane.INFORMATION_MESSAGE);

        } catch (DocumentException ex) {
            Logger.getLogger(ViewBillJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_generatePDFActionPerformed


